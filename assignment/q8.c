//WAP to swap 2 numbers.

#include<stdio.h>
int main()
{
    int a,b,c,d;
    printf("enter first number: ");
    scanf("%d",&a);
    printf("enter second number: ");
    scanf("%d",&b);
    c=b;
    d=a;
    printf("after swapping first number is %d\n",c);
    printf("after swapping second number is %d\n",d);
    return 0;
}