//WAP to find size of int, char, double,float.


#include<stdio.h>
int main()
{
    int a;
    char b;
    double c;
    float d;
    printf("size of int is %d bytes\n",sizeof(a));
    printf("size of char is %d bytes\n",sizeof(b));
    printf("size of double is %d bytes\n",sizeof(c));
    printf("size of float is %d bytes\n",sizeof(d));  
    return 0;  
}